-- Q7Find out the top 3 suppliers in terms of revenue generated by their products
SELECT s.id as supplierid,s.contactname,
sum(quantity*oi.unitprice)as totalrevenue
from supplier s
join product p on s.id=p.supplierid
join orderitem oi on oi.productid = p.id
