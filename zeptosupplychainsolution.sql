-- SECTION C : Level 3 (Advanced Queries)
-- Q1  Fetch the customer details who ordered more than 10 products in a single order
with product_ordered as (
     select distinct c.*,count(o.id) over
     (partition by c.id) as count
     from customer c
     join orders o on o.customerid = c.id
)
select * from product_ordered
where count > 10
order by count desc;

