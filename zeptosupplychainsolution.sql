-- SECTION B :LEVEL 2 (Intermediate queries)
-- Q1 Arrange the product ID and name based on the high demand by the customer
select p.id as product_id, p.productname,sum(oi.quantity)as total_quantity
from orderitem oi
join product p on p.id = oi.productid
group by p.id, p.productname
order by total_quantity desc;

-- Q2 Display the total number of orders delivered every year
select year(orderdate) as year, count(id) as
total_orders
